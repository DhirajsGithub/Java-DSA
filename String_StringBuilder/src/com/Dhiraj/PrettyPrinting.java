package com.Dhiraj;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 453.1274f;
        System.out.printf("formatted number is %.2f \n", a);    // ln --> f

        System.out.printf("Pie is : %.3f \n", Math.PI);

        System.out.printf("Hellow my name is %s and Iam %s ass", "Dhiraj", "Dumm");



    }
}


